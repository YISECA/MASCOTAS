                        <h3>Ubicación</h3>

                        <p align="justify" style="font-size: 10pt; line-height: 25px">Es el parque urbano más grande e importante de la ciudad de Bogotá,  se encuentra ubicado en el centro geográfico de Bogotá, (Colombia) en la localidad de Teusaquillo. Es surcado por las avenidas Carrera 60, Carrera 68 de oriente a occidente y las calles 63 y 53; es bastante popular entre los bogotanos quienes lo visitan a diario. Aunque el parque metropolitano se compone de varios parques, los bogotanos tienden a referirse a cada uno de estos parques de forma separada y no los asocian con un solo "megaparque".
                        En la actualidad se le considera el “pulmón de la ciudad”, por su estratégica ubicación en el corazón de Bogotá, por su amplia vegetación y gran dimensión de sus zonas verdes. Igualmente por la cantidad y variedad de escenarios que lo conforman.<br><br>
                        <strong>PARQUE CENTRAL SIMÓN BOLÍVAR</strong><br>
                        Es la principal zona verde del Parque Metropolitano Simón Bolívar, tiene un área de 113 hectáreas, en las cuales se pueden realizar distintas actividades del tipo contemplativo; el parque en sí fue construido en 1968 para la visita del papa Pablo VI, quien asistía al Congreso Eucarístico, en el parque se llevó a cabo una misa campal para lo cual se erigió el Templete Eucarístico ubicado frente al lago que tiene un área de 11 hectáreas; además de ello, el parque tiene dos áreas de juegos para niños, 16 kilómetros de caminos, pista para trote y una concha acústica como escenario complementario a la Plaza de Eventos, con capacidad para más de 80 mil espectadores, en donde en la actualidad se celebran eventos como el Festival de Verano, Rock al Parque y el Festival Iberoamericano de Teatro.
</p>

                        <h3>Descripción del recorrido </h3>

                        <p align="justify" style="font-size: 10pt; line-height: 25px">El recorrido dará inicio en la carrera 68 con calle 63, en este punto llegaran los participantes de la actividad <strong><i>(personas inscritas página web que tengan como mascota perros)</i></strong>  allí iniciaremos la actividad con una presentación institucional, recomendaciones específicas para la realización de la actividad y un calentamiento físico que permita la integración entre mascota y dueño.<br><br>

                        <strong>MI MASCOTA Y YO</strong><br>

                        Se iniciara el recorrido donde hablaremos la importancia de las mascotas y el cuidado de las mismas respetando su condición de animal y guardián, se resaltara la importancia de los perros como cuidadores y su función en la historia, allí a través de una dinámica cada dueño realizara una observación de su mascota acariciándola y reconociendo su característica más notable compartirá con el resto de los participantes cual es el rasgo más notable que le permite reconocer su mascota.
                        Se realiza una reflexión acerca de las responsabilidades que como dueño se tiene con sus mascotas.<br><br>

                        <strong>Rodeando el Lago</strong><br>

                        Continuaremos el recorrido por el sendero peatonal de tal manera que la brisa del agua permita a los visitantes relacionarse con las características de la naturaleza tales como el aire, sol o lluvia dependiendo de las condiciones climáticas del momento. Pasando por el puente de piedra se explicara la importancia de los mismos como conectores y nos dirigiremos hacia el templete donde a través de un pequeño trote cada dueño competirá con su mascota. Llegando a este punto se hablara de la historia del parque  teniendo en cuenta la siguiente información:<br>

                        <i>La historia del Parque Simón Bolívar se remonta al año de 1.968 cuando en terrenos del actual escenario se celebró una misa campal, como parte de la visita del papa Pablo VI a Colombia para el 39º Congreso Eucarístico Internacional, se construyó un pequeño Templete Eucarístico que sobrevive en la actualidad como parte de la geografía e historia del escenario, testigo del importante evento.<br>

                        Jurídicamente el parque nació según la Ley 31 de 1979 para conmemorar los 200 años del nacimiento de Bolívar a celebrarse 4 años más tarde, con lo que se dejaba un tiempo prudencial para la construcción del nuevo parque.<br>

                        La primera etapa se entregó el 22 de julio de 1.983 con la culminación de la plaza ceremonial conocida actualmente como Plaza de Eventos. Escenario con capacidad para 80 mil espectadores. Además, se entregó la siembra de 3.300 árboles y la iluminación artificial.<br>

                        La segunda fase de acometidas se empezó a entregar en diciembre de ese mismo año, con la remodelación del Templete Eucarístico. Además, quedó listo el puente peatonal que comunica al parque Central con el Museo de los Niños y fue instalado el busto de bolívar donado por el gobierno Venezolano de entonces, con 2,20 metros de alto y 8 toneladas de peso.<br>

                        Otro año decisivo para el Central Simón Bolívar fue el de 1.986 cuando se celebró un gigantesco acto litúrgico a cielo abierto en las instalaciones del parque, gracias a la llegada al país del papa Juan Pablo II. Y el 15 de diciembre de 1991 se inauguró de manera oficial el Parque Central Simón Bolívar.</i><br><br>

                        <strong>CAMINANDO ANDO Y DE PLANTAS VOY HABLANDO</strong><br>

                        Continuamos por el sendero haciendo una descripción de la vegetación más representativa, allí cada perro y su dueño identificaran diferentes tipos de árboles y arbustos encontrados en el entorno, se percibirán aromas y formas de las especies vegetales encontradas, cada participante junto con su mascota  escogerán un árbol y lo alimentaran con el material muerto encontrado en el suelo, el guía dará las especificaciones.<br><br>

                        <strong>PERROS EMBLEMÁTICAS MASCOTAS</strong><br>

                        Continuamos nuestro recorrido hacia una zona verde del parque donde nos reuniremos y a través de un ejercicio de recuerdo traeremos a nuestra memoria aquellos perros o mascotas emblemáticas que durante años han sido recordadas por su valentía y fidelidad, Hachico, Rex, Lassi, Rin tin tin, Balto entre otros, de esta manera se destaca la capacidad e importancia de los perros que más que ser una mascota son animales con capacidades excepcionales.<br>

                        Continuamos nuestro recorrido hacia el monumento Bolivariano, donde se realizara una reseña histórica y se continuara hacia la plaza de eventos, en este punto se realiza el cierre de la actividad con los respectivos agradecimientos y la invitación a participar de las diferentes actividades realizadas por el Instituto de Recreación y Deporte (IDRD).<br>
</p>

